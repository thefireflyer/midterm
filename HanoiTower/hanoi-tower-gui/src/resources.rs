use bevy::{ecs::system::Resource, time::Timer};

///////////////////////////////////////////////////////////////////////////////

#[derive(Resource)]
pub struct TowerConfig {
    pub number_of_towers: u16,
    pub number_of_disks: u16,
    pub running: bool,
    pub speed: f32,
    pub moves: Vec<(usize, usize, u32)>,
    pub timer: Timer,
}

